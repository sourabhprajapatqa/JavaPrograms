package package2;

public class Assign77 {
    public static void main(String[] args) {
        int num = 130;  // Example int value
        byte byteValue = (byte) num;  // Narrowing (casting)

        System.out.println("Int value: " + num);
        System.out.println("Byte value after narrowing: " + byteValue);
    }
}
