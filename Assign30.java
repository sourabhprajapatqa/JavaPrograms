public class Assign30 {
    public static void main(String[] args) {
        // Initialize an array
        int[] arr = {1, 2, 3, 4, 5};

        // Update the value at index 2
        arr[2] = 10; // Changing value from 3 to 10

        // Display the updated array
        System.out.println("Updated array:");
        for (int value : arr) {
            System.out.print(value + " ");
        }
    }
}
